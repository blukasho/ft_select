#include "ft_select.h"

int			read_input(t_ft_select *s)
{
	long	buf;

	if (s)
	{}
	while (!0 && buf != 27 && !(buf = 0))
	{
		if (read(STDIN_FILENO, &buf, 4))
			ft_printf("%ld ", buf);
	}
	return (0);
}
